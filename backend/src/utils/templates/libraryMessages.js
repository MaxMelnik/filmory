import escapeReservedCharacters from '../escapeReservedCharacters.js';

export function getWatchedMessage(title, count, rate = 0) {
    title = escapeReservedCharacters(title);
    count = escapeReservedCharacters(count);
    rate = escapeReservedCharacters(String(rate));
    const ratedMessage = title ? `‚≠ê –û—Ü—ñ–Ω–∏–≤ *¬´${title}¬ª* –Ω–∞ ${rate}/10\\. –ì–∞—Ä–Ω–∏–π –≤–∏–±—ñ—Ä\\!\n\n` : '';
    if (count === 1) {
        return `${ratedMessage}–¶–µ —Ç–≤—ñ–π –ø–µ—Ä—à–∏–π —Ñ—ñ–ª—å–º —É Filmory ‚Äì –≥–∞—Ä–Ω–∏–π —Å—Ç–∞—Ä—Ç –¥–ª—è –æ—Å–æ–±–∏—Å—Ç–æ—ó —Ñ—ñ–ª—å–º–æ—Ç–µ–∫–∏\\!`;
    }

    if (count < 5) {
        return `${ratedMessage}–ó–∞—Ä–∞–∑ —É —Ç–µ–±–µ –≤ –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏—Ö —É–∂–µ *${count}* —Ñ—ñ–ª—å–º–∏ ‚Äì –¥—É–∂–µ –Ω–µ–ø–æ–≥–∞–Ω–∏–π –ø–æ—á–∞—Ç–æ–∫\\!`;
    }

    if (count < 10) {
        return `${ratedMessage}–£ —Ç–≤–æ—ó–π –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç—ñ–π –∫–æ–ª–µ–∫—Ü—ñ—ó –≤–∂–µ *${count}* —Ñ—ñ–ª—å–º—ñ–≤\\. –ü–æ—Ç—Ä–æ—Ö—É —Ñ–æ—Ä–º—É—î—Ç—å—Å—è —Ç–≤—ñ–π –∫—ñ–Ω–æ—Å–º–∞–∫\\.`;
    }

    if (count < 20) {
        return `${ratedMessage}*${count}* –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏—Ö —Ñ—ñ–ª—å–º—ñ–≤ —É Filmory ‚Äì –∑–≤—É—á–∏—Ç—å —è–∫ –ø–æ—á–∞—Ç–æ–∫ —Å–µ—Ä–π–æ–∑–Ω–æ—ó —Ñ—ñ–ª—å–º–æ—Ç–µ–∫–∏\\.`;
    }

    if (count < 50) {
        return `${ratedMessage}–£ —Ç–µ–±–µ –≤–∂–µ *${count}* –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏—Ö —Ñ—ñ–ª—å–º—ñ–≤\\. –¶–µ —Ä—ñ–≤–µ–Ω—å —É–≤–∞–∂–Ω–æ–≥–æ –≥–ª—è–¥–∞—á–∞ –∑ —á—ñ—Ç–∫–∏–º —Å–º–∞–∫–æ–º\\.`;
    }

    if (count < 100) {
        return `${ratedMessage}–ó–∞—Ä–∞–∑ —É —Ç–µ–±–µ –≤ –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏—Ö *${count}* —Ñ—ñ–ª—å–º—ñ–≤ ‚Äì —Ü–µ –≤–∂–µ —Ñ—ñ–ª—å–º–æ—Ç–µ–∫–∞ —Å–ø—Ä–∞–≤–∂–Ω—å–æ–≥–æ –∫—ñ–Ω–æ–º–∞–Ω–∞\\!`;
    }

    if (count < 200) {
        return `${ratedMessage}*${count}* –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏—Ö —Ñ—ñ–ª—å–º—ñ–≤ —É Filmory\\. –¶–µ –≤–∂–µ –Ω–µ –ø—Ä–æ—Å—Ç–æ —Ö–æ–±—ñ ‚Äì —Ü–µ –ø–æ–≤–Ω–æ—Ü—ñ–Ω–Ω–∞ –∫—ñ–Ω–æ–∫–æ–ª–µ–∫—Ü—ñ—è\\.`;
    }

    return `${ratedMessage}*${count}* –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏—Ö —Ñ—ñ–ª—å–º—ñ–≤‚Ä¶ –ù–∞ —Ç–∞–∫–æ–º—É —Ä—ñ–≤–Ω—ñ —Ç–µ–±–µ –≤–∂–µ –º–æ–∂–Ω–∞ –æ—Ñ—ñ—Ü—ñ–π–Ω–æ –∑–∞–ø–∏—Å—É–≤–∞—Ç–∏ –≤ –∫—ñ–Ω–æ–∫—Ä–∏—Ç–∏–∫–∏\\.`;
}

